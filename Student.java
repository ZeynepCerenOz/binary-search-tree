public class Student {
    int ID;
    String name;
    String surname;
    int age;
    double GPA;

    public Student(int ID, String name, String surname, int age, double GPA){
        this.ID=ID;
        this.name=name;
        this.surname=surname;
        this.age=age;
        this.GPA=GPA;
    }
}
